        // DOM elements
        const startBtn = document.getElementById('start-btn');
        const nextBtn = document.getElementById('next-btn');
        const restartBtn = document.getElementById('restart-btn');
        const quizOptions = document.querySelectorAll('.quiz-option');
        const screens = document.querySelectorAll('.screen');
        const questionEl = document.getElementById('question');
        const optionsEl = document.getElementById('options');
        const feedbackEl = document.getElementById('feedback');
        const progressBar = document.getElementById('progress-bar');
        const pointsEl = document.getElementById('points');
        const levelEl = document.getElementById('level');
        const streakEl = document.getElementById('streak');
        const finalScoreEl = document.getElementById('final-score');
        const userScoreEl = document.getElementById('user-score');

        // Game state
        let currentQuizType = '';
        let currentQuestion = 0;
        let score = 0;
        let points = 0;
        let streak = 0;
        let level = 1;
        let selectedQuestions = [];

        // Event listeners
        startBtn.addEventListener('click', startQuiz);
        nextBtn.addEventListener('click', nextQuestion);
        restartBtn.addEventListener('click', restartQuiz);
        
        quizOptions.forEach(option => {
            option.addEventListener('click', () => {
                quizOptions.forEach(o => o.style.border = 'none');
                option.style.border = '3px solid #3498db';
                currentQuizType = option.dataset.type;
            });
        });

        // Functions
        function startQuiz() {
            if (!currentQuizType) {
                alert('Please select a quiz type first!');
                return;
            }
            
            // Select 10 random questions from the chosen category
            selectedQuestions = getRandomQuestions(quizData[currentQuizType], 10);
            
            showScreen('quiz-screen');
            loadQuestion();
        }

        function getRandomQuestions(questions, count) {
            // Create a copy of the questions array to avoid modifying the original
            const shuffled = [...questions].sort(() => 0.5 - Math.random());
            return shuffled.slice(0, count);
        }

        function loadQuestion() {
            if (currentQuestion >= selectedQuestions.length) {
                endQuiz();
                return;
            }
            
            const question = selectedQuestions[currentQuestion];
            questionEl.textContent = question.question;
            
            // Update progress bar
            progressBar.style.width = `${((currentQuestion + 1) / selectedQuestions.length) * 100}%`;
            
            // Clear options
            optionsEl.innerHTML = '';
            
            // Add options
            question.options.forEach((option, index) => {
                const optionEl = document.createElement('div');
                optionEl.classList.add('option');
                optionEl.textContent = option;
                optionEl.dataset.index = index;
                optionEl.addEventListener('click', selectOption);
                optionsEl.appendChild(optionEl);
            });
            
            // Reset UI state
            nextBtn.disabled = true;
            feedbackEl.style.display = 'none';
        }

        function selectOption(e) {
            const selectedOption = e.target;
            const options = document.querySelectorAll('.option');
            const correctIndex = selectedQuestions[currentQuestion].answer;
            
            // Mark selected option
            options.forEach(option => option.classList.remove('selected'));
            selectedOption.classList.add('selected');
            
            // Check if correct
            const isCorrect = parseInt(selectedOption.dataset.index) === correctIndex;
            
            // Show feedback
            feedbackEl.textContent = isCorrect ? 'Correct! Well done!' : 'Incorrect. Try again next time!';
            feedbackEl.className = 'feedback ' + (isCorrect ? 'correct' : 'incorrect');
            feedbackEl.style.display = 'block';
            
            // Update game state
            if (isCorrect) {
                score += 10;
                points += 20;
                streak += 1;
                
                // Level up logic
                if (points >= level * 100) {
                    level += 1;
                    levelEl.textContent = `Level ${level}`;
                }
                
                // Update stats
                pointsEl.textContent = points;
                streakEl.textContent = streak;
            } else {
                streak = 0;
                streakEl.textContent = streak;
            }
            
            // Enable next button
            nextBtn.disabled = false;
        }

        function nextQuestion() {
            currentQuestion++;
            loadQuestion();
        }

        function endQuiz() {
            showScreen('results-screen');
            finalScoreEl.textContent = score;
            userScoreEl.textContent = `${score} pts`;
        }

        function restartQuiz() {
            currentQuestion = 0;
            score = 0;
            showScreen('welcome-screen');
            
            // Reset UI
            quizOptions.forEach(o => o.style.border = 'none');
            currentQuizType = '';
            pointsEl.textContent = '0';
            streakEl.textContent = '0';
            levelEl.textContent = 'Level 1';
        }

        function showScreen(screenName) {
            screens.forEach(screen => {
                screen.classList.remove('active');
            });
            document.querySelector(`.${screenName}`).classList.add('active');
        }